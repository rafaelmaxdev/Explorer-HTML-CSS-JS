// multiplicação
console.log(3 * 5); // 15

// divisão
console.log(10 / 2); // 5

// soma
console.log(10 + 3); // 13

//subtração
console.log(10 - 3); // 7

// resto da divisão
console.log(10 % 3); // 1

// incremento e decremento
let increment = 4;
let increment2 = 4;
console.log(++increment); // 5
console.log(--increment2); // 3

// exponencial
console.log(3 ** 3); // 27
