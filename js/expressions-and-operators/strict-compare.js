let one = 1;
let two = 2;

//  === estritamente igual a
console.log(one == "1"); // true // valor igual e tipo diferente
console.log(one === "1"); // false // valor igual e tipo diferente
console.log(one === 1); // true // mesmo valor e mesmo tipo

//  !=== estritamente igual a
console.log(one !== "1"); // true // valor igual e tipo diferente
console.log(one !== 1); // false // mesmo valor e mesmo tipo
